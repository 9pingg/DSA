void print_v(vector<int> &v){
    for(auto val : v){
        cout << val << " ";
    }
    cout << endl;
}


void print_m(map<int, int> &m){
    for(auto val : m){
        cout <<val.first << " " << val.second << endl;
    }
    cout << endl;
}

